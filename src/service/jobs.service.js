import axios from "axios";
const basePath = "jobs";

const JobsService = {  
  getJobs() {
    return new Promise((resolve, reject) => {
      axios
        .get(`/${basePath}`)
        .then((result) => {
          resolve(result);
        })
        .catch((error) => {
          if (error.response) {
            reject(error.response);
          } else {
            reject(new Error(error));
          }
        });
    });
  },
  getJobDetails(id) {
    return new Promise((resolve, reject) => {
      axios
        .get(`/${basePath}/${id}`)
        .then((result) => {
          resolve(result);
        })
        .catch((error) => {
          if (error.response) {
            reject(error.response);
          } else {
            reject(new Error(error));
          }
        });
    });
  },
  apply(data) {
    return new Promise((resolve, reject) => {
      axios
        .post(`/${basePath}/applications`, data)
        .then((result) => {
          resolve(result);
        })
        .catch((error) => {
          if (error.response) {
            reject(error.response);
          } else {
            reject(new Error(error));
          }
        });
    });
  },
  getAppliedJobs(slug = "") {
    return new Promise((resolve, reject) => {
      axios
        .get(`/${basePath}/applications${slug}`)
        .then((result) => {
          resolve(result);
        })
        .catch((error) => {
          if (error.response) {
            reject(error.response);
          } else {
            reject(new Error(error));
          }
        });
    });
  },
};

export default JobsService;
